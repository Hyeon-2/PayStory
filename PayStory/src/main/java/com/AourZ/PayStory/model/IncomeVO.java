package com.AourZ.PayStory.model;

import java.util.Date;

public class IncomeVO {
	private String incomeNo;
	private Date incomeDate;
	private String incomeSource;
	private String incomeMemo;
	private int incomeAmount;
	private String tagNo;
	private String memberNo;
	private String editNo;
	private String accountBookNo;
	
	public String getIncomeNo() {
		return incomeNo;
	}
	public void setIncomeNo(String incomeNo) {
		this.incomeNo = incomeNo;
	}
	public Date getIncomeDate() {
		return incomeDate;
	}
	public void setIncomeDate(Date incomeDate) {
		this.incomeDate = incomeDate;
	}
	public String getIncomeSource() {
		return incomeSource;
	}
	public void setIncomeSource(String incomeSource) {
		this.incomeSource = incomeSource;
	}
	public String getIncomeMemo() {
		return incomeMemo;
	}
	public void setIncomeMemo(String incomeMemo) {
		this.incomeMemo = incomeMemo;
	}
	public int getIncomeAmount() {
		return incomeAmount;
	}
	public void setIncomeAmount(int incomeAmount) {
		this.incomeAmount = incomeAmount;
	}
	public String getTagNo() {
		return tagNo;
	}
	public void setTagNo(String tagNo) {
		this.tagNo = tagNo;
	}
	public String getMemberNo() {
		return memberNo;
	}
	public void setMemberNo(String memberNo) {
		this.memberNo = memberNo;
	}
	public String getEditNo() {
		return editNo;
	}
	public void setEditNo(String editNo) {
		this.editNo = editNo;
	}
	public String getAccountBookNo() {
		return accountBookNo;
	}
	public void setAccountBookNo(String accountBookNo) {
		this.accountBookNo = accountBookNo;
	}
	
	
}


